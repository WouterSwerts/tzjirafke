        <p>Zodra we merken dat er interesse is in het potje, starten we met zindelijkheidstraining. Meestal begint dit als ze de 24 manden gepasseerd zijn. Wij hopen natuurlijk dat er dan thuis ook geoefend wordt. Indien het kindje hiervoor nog niet klaar is stellen we dit even uit en proberen we later opnieuw. Als ze op het potje geweest zijn mogen ze een stickertje plakken op hun eigen blad.</p>
